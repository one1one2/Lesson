function [A F] = example3(x)
A{1}.f = x(1)^2 + x(2)^2 + 2*x(3)^2 + x(4)^2 ...
        - 5*x(1) -5*x(2) -21*x(3) + 7*x(4);
A{1}.g = zeros(4,1);
A{1}.g(1) = 2*x(1) - 5;
A{1}.g(2) = 2*x(2) - 5;
A{1}.g(3) = 4*x(3) - 21;
A{1}.g(4) = 2*x(4) + 7;
A{1}.G = 2*eye(4);
A{1}.G(3,3) = 4;

A{2}.f = -x(1)^2-x(2)^2-x(3)^2-x(4)^2-x(1)+x(2)-x(3)+x(4)+8;
A{2}.g = zeros(4,1);
A{2}.g(1) = -2*x(1) - 1;
A{2}.g(2) = -2*x(2) + 1;
A{2}.g(3) = -2*x(3) - 1;
A{2}.g(4) = -2*x(4) + 1;
A{2}.G = -2*eye(4);

alpha2 = 10;
A{2}.f = A{1}.f - alpha2*A{2}.f;
A{2}.g = A{1}.g - alpha2*A{2}.g;
A{2}.G = A{1}.G - alpha2*A{2}.G;

A{3}.f = -x(1)^2-2*x(2)^2-x(3)^2-2*x(4)^2+x(1)+x(4)+10;
A{3}.g = zeros(4,1);
A{3}.g(1) = -2*x(1) + 1;
A{3}.g(2) = -4*x(2);
A{3}.g(3) = -2*x(3);
A{3}.g(4) = -4*x(4) + 1;
A{3}.G = -2*eye(4);
A{3}.G(2,2) = -4;
A{3}.G(4,4) = -4;

alpha3 = 10;
A{3}.f = A{1}.f - alpha3*A{3}.f;
A{3}.g = A{1}.g - alpha3*A{3}.g;
A{3}.G = A{1}.G - alpha3*A{3}.G;

A{4}.f = -x(1)^2-x(2)^2-x(3)^2-2*x(1)+x(2)+x(4)+5;
A{4}.g = zeros(4,1);
A{4}.g(1) = -2*x(1) - 2;
A{4}.g(2) = -2*x(2) + 1;
A{4}.g(3) = -2*x(3);
A{4}.g(4) = 1;
A{4}.G = -2*eye(4);
A{4}.G(4,4) = 0;

alpha4 = 10;
A{4}.f = A{1}.f - alpha4*A{4}.f;
A{4}.g = A{1}.g - alpha4*A{4}.g;
A{4}.G = A{1}.G - alpha4*A{4}.G;

F = max(A{1}.f, A{2}.f);
F = max(F, A{3}.f);
F = max(F, A{4}.f);
end 


